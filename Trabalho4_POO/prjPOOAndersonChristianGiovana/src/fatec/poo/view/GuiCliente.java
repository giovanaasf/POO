    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String cpf = ftxtCpf.getText();
                
        while(cpf.indexOf(".") > 0)
            cpf = cpf.replace(".", "");
        
        cpf = cpf.replace("-", "");
        
        if(!Pessoa.validarCPF(cpf)){
            JOptionPane.showMessageDialog(this, "CPF inválido", "Erro", 2);
             this.ftxtCpf.grabFocus();
        }else{
            Cliente cliente = this.daoCliente.consultar(cpf);

            if(cliente == null)
                this.ableTxtAfterConult();
            else{
                
                this.btnConsultar.setEnabled(false);
                this.btnAlterar.setEnabled(true);
                this.btnExcluir.setEnabled(true);
                
                this.ftxtCpf.setEnabled(false);

                this.txtNome.setEnabled(true);
                this.txtEndereco.setEnabled(true);
                this.txtCidade.setEnabled(true);
                this.cbxUf.setEnabled(true);
                this.txtCep.setEnabled(true);
                this.txtDdd.setEnabled(true);
                this.txtTelefone.setEnabled(true);
                this.txtLimiteCredito.setEnabled(true);

                this.txtNome.setText(cliente.getNome());
                this.txtEndereco.setText(cliente.getEndereco());
                this.txtCidade.setText(cliente.getCidade());
                this.cbxUf.setSelectedItem(cliente.getUf());
                this.txtCep.setText(cliente.getCep());
                this.txtDdd.setText(cliente.getDdd());
                this.txtTelefone.setText(cliente.getTelefone());
                this.txtLimiteCredito.setText(Double.toString(cliente.getLimiteCred()));
                this.lblLimiteDisponivelValor.setText(Double.toString(cliente.getLimiteDisp()));
                
                
                
                
                this.txtNome.grabFocus();
            }
        }
        
    }//GEN-LAST:event_btnConsultarActionPerformed

