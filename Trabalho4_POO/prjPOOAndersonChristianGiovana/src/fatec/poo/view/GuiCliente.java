    private Cliente getClienteByGui(){
        Cliente cliente = null;
        
        String cpf = this.ftxtCpf.getText();
        String nome = this.txtNome.getText();
        String endereco = this.txtEndereco.getText();
        String cidade = this.txtCidade.getText();
        String cep = this.txtCep.getText();
        String uf = this.cbxUf.getItemAt(this.cbxUf.getSelectedIndex());
        String telefoneddd = this.txtDdd.getText(); 
        String telefonenumero = this.txtTelefone.getText();

        double limitecredito = Double.parseDouble(this.txtLimiteCredito.getText());
                
        while(cpf.indexOf(".") > 0)
            cpf = cpf.replace(".", "");
        
        cpf = cpf.replace("-", "");
        
        cliente = new Cliente(cpf, nome, limitecredito);
        cliente.setEndereco(endereco);
        cliente.setCidade(cidade);
        cliente.setCep(cep);
        cliente.setUf(uf);
        cliente.setDdd(telefoneddd);
        cliente.setTelefone(telefonenumero);
        
        return cliente;
    }
    
